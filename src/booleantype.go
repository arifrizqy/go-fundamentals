package main

import "fmt"

func main() {
	// Tipe data boolean adalah tipe data yang paling sederhana pada semua bahasa pemrograman.
	// Karena tipe data boolean hanya memiliki dua nilai.
	// Tipe data boolean direpresentasikan dengan kata kunci `bool`.
	// | Nilai Boolean | Keterangan |
	// true | benar
	// false | salah

	fmt.Println("[Boolean/bool] benar:", true)
	fmt.Println("[Boolean/bool] salah:", false)
}
